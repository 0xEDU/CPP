/* Canonical Form =========================================================== */
RPN::RPN() {
	return ;
}

RPN::RPN(const RPN &rhs) {
	*this = rhs;
	return ;
}

RPN &RPN::operator=(const RPN &rhs) {
	this->_input = rhs._input;
	return *this;
}

RPN::~RPN() {
	return ;
}
